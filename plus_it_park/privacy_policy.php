<h3>How We Use Personal Data</h3>
<p style="text-align: justify;">We adhere to a stringent policy and governance framework concerning the utilization of
personal data and information provided by our users.</p>
<p style="text-align: justify;">The entered data is securely stored in isolated databases with multiple layers of data security
and access control measures in place.</p>
<p style="text-align: justify;">The primary purpose of utilizing this data is to enhance the delivery of our products and
services, ensuring customized service delivery. Importantly, the data is not employed for
sending news feeds unless users have explicitly opted for it. As advocates against spam mails,
our data handling policy guarantees that user data will never be used for sending unsolicited
messages.</p>
<p style="text-align: justify;">The data is put to use for several purposes:</p>
<ul>
