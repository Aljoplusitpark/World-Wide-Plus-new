<p style="text-align: justify;">Our commitment is to ensure that you have complete access and control over the data,
providing a seamless and user-friendly experience through the dashboard or support team
interaction</p>
<h3>Cookies and Other</h3>
<p style="text-align: justify;">At Plus IT Business Park, we leverage various technologies to facilitate user interactions, and
key among them are Cookies, Web Beacons, and Log Files.</p>
<p style="text-align: justify;">Cookies, which are small text files stored on the local computer by the browser under the
instruction of the website, play a crucial role. These files are retrieved when the domain or page
is loaded, and their primary use is to provide customized personalization to our products.
</p>
<p style="text-align: justify;">On the other hand, Logs are maintained to ensure the safety and security of our operations, as
well as for debugging purposes when necessary. The information contained in logs includes the
IP address of the request, the type of request, and technical details such as headers and status
codes.</p>
<p style="text-align: justify;">In addition to this, server-side session management is implemented to handle session and
client-based information. This ensures the delivery of customized services to enhance the
overall user experience.</p>
<h3>Data Removal Policies</h3>
<p style="text-align: justify;">The Data Removal Policies outline various guidelines governing the removal of data from the
records of Plus IT Business Park and its products.</p>
<p style="text-align: justify;">Data removal, as defined, involves erasing any records related to an entity from Plus IT
Business Park and its products. Users can voluntarily initiate the removal process through the
dashboard, by emailing info@plusitpark.com, or by contacting the Plus Safe Center.</p>
<p style="text-align: justify;">Additionally, data removal may be triggered by legal actions in accordance with the Constitution
of the Republic of India and at the request of law enforcement agencies. The policy also
acknowledges circumstances where data removal is deemed ethical or aligns with a code of
ethics.</p>
<p style="text-align: justify;">Furthermore, the data removal policy may be enforced if the data owner is aged 13 or below</p>
<p style="text-align: justify;">For more information, please don't hesitate to reach out to the Plus Safe Center.</p>
<h3>Data Policy Regarding Users Below age of 13</h3>
<p style="text-align: justify;">The IT Business Park has a policy in place that explicitly states it does not collect any data from
individuals under the age of 13. This is in line with privacy regulations and standards that
recognize the need for special considerations when handling data related to minors.</p>
<p style="text-align: justify;">Moreover, the company takes a proactive stance by not offering any products or services that
target children, particularly those under the age of 13. This decision is aligned with the
company's commitment to responsible data practices and safeguards against any inadvertent
collection of information from this age group.</p>
<p style="text-align: justify;">Recognizing the sensitivity involved in handling data of child users, the IT Business Park
acknowledges the importance of exercising extra caution and diligence in this regard. This
emphasizes the company's commitment to protecting the privacy and security of young users.
</p>
<p style="text-align: justify;">In the event that a user is identified as being under the age of 13, the company assures that
such users are granted all the privileges related to data security. This includes providing them
with an easy and accessible option to remove their data from the company's servers. This
additional step reflects the company's commitment to giving users, particularly minors, control
over their personal information and ensuring a transparent and secure data handling process.</p>
