information, each handled with distinct protocols. Personally Identifiable Information (PII) and
Non-personally Identifiable Information (non-PII) are categorized, with stringent measures
applied to protect user privacy. The company offers users the option to delete their data through
various means, including user requests, law enforcement agency requests, or when there are
valid grounds for deletion.</p>
<p style="text-align: justify;">The company's commitment to responsible data practices extends to how personal data is used.
It is securely stored with multiple layers of security, and its utilization is focused on enhancing
